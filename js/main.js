	// Volume slider

	const volumeSlider = document.getElementById('volume-slider')

	function updateProgressVolume(percentage) {
		const progressPercent = percentage * 100
		volumeSlider.querySelector('.volume-slider-progress').style.width =
			progressPercent + '%'
	}

